    private void salaryAddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salaryAddBtnActionPerformed
        // TODO add your handling code here:
        if ("".equals(MaLuongCBB.getSelectedItem().toString()) || "".equals(MaNVCBB.getSelectedItem().toString())) {
            JOptionPane.showMessageDialog(this, "Không được bỏ trống dữ liệu nhập vào !!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            bldto.setMaLuong(MaLuongCBB.getSelectedItem().toString());
            bldto.setMaNV(MaNVCBB.getSelectedItem().toString());

            blbus.addBangLuong(bldto);
            JOptionPane.showMessageDialog(this, "Thêm lương thành công !!!");
            defaultTableModel.setRowCount(0);
            setSalaryTable(blbus.getBangLuong());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e, "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_salaryAddBtnActionPerformed
    
