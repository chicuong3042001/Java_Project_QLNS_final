    public void setSalaryTable(ArrayList<BangLuongDTO> SalaryList) {
        for (BangLuongDTO SalaryItem : SalaryList) {
            defaultTableModel.addRow(new Object[]{
                SalaryItem.getMaLuong(),
                SalaryItem.getMaNV(),
                SalaryItem.getTenNV(),
                SalaryItem.getLuongCoBan(),
                SalaryItem.getHeSoLuong(),
                SalaryItem.getThuongPhat(),
                SalaryItem.getTienLuong(),
                SalaryItem.getNgayLapBang(),
                SalaryItem.getNgaySuaDoi()
            });
        }
    }

    public void getMaLuongCBB(ArrayList<LuongDTO> CBBItems) {
        MaLuongCBB.removeAllItems();
        for (LuongDTO CBBItem : CBBItems) {
            MaLuongCBB.addItem(CBBItem.getMaLuong());
        }
    }

    public void getMaNVCBB(ArrayList<NhanVienDTO> CBBItems) {
        MaNVCBB.removeAllItems();
        for (NhanVienDTO CBBItem : CBBItems) {
            MaNVCBB.addItem(CBBItem.getMaNV());
        }
    }

    public void getAllCBB() {
        LuongBUS luongBUS = new LuongBUS();
        ArrayList<LuongDTO> arrLuong = luongBUS.getLuong();
        getMaLuongCBB(arrLuong);

        NhanVienBUS nvbus = new NhanVienBUS();
        ArrayList<NhanVienDTO> arrNV = nvbus.getNhanVien();
        getMaNVCBB(arrNV);
    }

