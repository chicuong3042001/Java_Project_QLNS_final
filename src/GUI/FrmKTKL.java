    private void KTKLDeleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_KTKLDeleteBtnActionPerformed
        // TODO add your handling code here:
        int row = dataTable.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn bảng KTKL cần xóa !!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } else {
            int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa không ?");

            if (confirm == JOptionPane.YES_OPTION) {
                String KTKLCode = String.valueOf(dataTable.getValueAt(row, 0));
                
                try {
                    ktklBUS.deleteKhenThuongKyLuat(KTKLCode);
                } catch (AllException ex) {
                    JOptionPane.showMessageDialog(this, ex,"Lỗi",JOptionPane.ERROR_MESSAGE);
                }
                defaultTableModel.setRowCount(0);
                setKTKLData(ktklBUS.getKhenThuongKyLuat());
            }
        }
    }//GEN-LAST:event_KTKLDeleteBtnActionPerformed

    private void KTKLSaveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_KTKLSaveBtnActionPerformed
        // TODO add your handling code here:
        int row = dataTable.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn khen thưởng kỷ luật cần sửa !!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            ktklDTO.setMaKTKL(KTKLIDField.getText());
            ktklDTO.setSoTien(Double.parseDouble(KTKLMoneyField.getText()));
            ktklDTO.setLoaiQuyetDinh(KTKLCBB.getSelectedItem().toString());
            ktklDTO.setNoiDung(KTKLtxtArea.getText());
            
            ktklBUS.updateKhenThuongKyLuat(ktklDTO);
            JOptionPane.showMessageDialog(this, "Sửa khen thưởng kỷ luật thành công !!!");
            defaultTableModel.setRowCount(0);
            setKTKLData(ktklBUS.getKhenThuongKyLuat());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    }//GEN-LAST:event_KTKLSaveBtnActionPerformed

