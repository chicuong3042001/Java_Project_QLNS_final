    public void showInfo() {
        int row = dataTable.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn khen thưởng kỷ luật cần sửa !!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                String KTKLCode = String.valueOf(dataTable.getValueAt(row, 0));
            ktklDTO = ktklBUS.getKTKLByID(KTKLCode);
            KTKLIDField.setText(ktklDTO.getMaKTKL());
            KTKLIDField.setEnabled(false);
            KTKLMoneyField.setText(String.valueOf(ktklDTO.getSoTien()));
            KTKLCBB.setSelectedItem(ktklDTO.getLoaiQuyetDinh());
            KTKLtxtArea.setText(ktklDTO.getNoiDung());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
            
        }
    }
    
