    private void HSLAddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HSLAddBtnActionPerformed
        // TODO add your handling code here:
        if ("".equals(HSLIDField.getText()) || "".equals(HSLField.getText())) {
            JOptionPane.showMessageDialog(this, "Không được bỏ trống dữ liệu nhập vào !!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            hsldto.setMaHSL(HSLIDField.getText());
            try {
                hsldto.setHeSoLuong(Double.parseDouble(HSLField.getText()));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Hệ số lương bạn vừa nhập không phải là số, vui lòng nhập đúng !!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
                return;
            }
            hslbus.addHeSoLuong(hsldto);
            JOptionPane.showMessageDialog(this, "Thêm hệ số lương thành công !!!");
            defaultTableModel.setRowCount(0);
            setHSL(hslbus.getHeSoLuong());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Mã hệ số lương bị trùng", "Lỗi", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_HSLAddBtnActionPerformed

    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        String input = inputField.getText().trim();
        if ("".equals(inputField.getText())) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mã KTKL cầm tìm !!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        dataTable.removeAll();
        defaultTableModel.setRowCount(0);
        setHSLDataByID(hslbus.getHeSoLuong(), input);
    }//GEN-LAST:event_searchBtnActionPerformed

    public void setHSLDataByID(ArrayList<HeSoLuongDTO> HSLs, String MaHSL) {
        for (HeSoLuongDTO HSL : HSLs) {
            MaHSL = MaHSL.toLowerCase();
            if (HSL.getMaHSL().toLowerCase().equals(MaHSL)) {
                defaultTableModel.addRow(new Object[]{
                    HSL.getMaHSL(),
                    HSL.getHeSoLuong(),});
            }
        }
    }

