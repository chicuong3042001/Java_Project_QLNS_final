    public void showInfo() {
        int row = dataTable.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn hệ số lương cần sửa !!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } else {
            String HSLCode = String.valueOf(dataTable.getValueAt(row, 0));
            hsldto = hslbus.findHSLbyID(HSLCode);
            HSLIDField.setText(hsldto.getMaHSL());
            HSLIDField.setEnabled(false);
            HSLField.setText(String.valueOf(hsldto.getHeSoLuong()));
        }
    }

