    private void HSLDeleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HSLDeleteBtnActionPerformed
        // TODO add your handling code here:
        int row = dataTable.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn hệ số lương cần xóa !!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa không ?");

                if (confirm == JOptionPane.YES_OPTION) {
                    String HSLCode = String.valueOf(dataTable.getValueAt(row, 0));
                    hslbus.deleteHeSoLuong(HSLCode);
                    defaultTableModel.setRowCount(0);
                    setHSL(hslbus.getHeSoLuong());
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e);
            }

        }
    }//GEN-LAST:event_HSLDeleteBtnActionPerformed

    private void HSLSaveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HSLSaveBtnActionPerformed
        // TODO add your handling code here:
        int row = dataTable.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn hệ số lương cần sửa !!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            try {
                hsldto.setMaHSL(HSLIDField.getText());
                hsldto.setHeSoLuong(Double.parseDouble(HSLField.getText()));
                hslbus.updateHeSoLuong(hsldto);
                JOptionPane.showMessageDialog(this, "Sửa hệ số lương thành công !!!");
                defaultTableModel.setRowCount(0);
                setHSL(hslbus.getHeSoLuong());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Hệ số lương bạn vừa nhập không phải là số, vui lòng nhập đúng !!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    }//GEN-LAST:event_HSLSaveBtnActionPerformed

