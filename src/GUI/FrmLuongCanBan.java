    private void salaryAddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salaryAddBtnActionPerformed
        // TODO add your handling code here:
        if ("".equals(salaryIDField.getText()) || "".equals(salaryField.getText())) {
            JOptionPane.showMessageDialog(this, "Không được bỏ trống dữ liệu nhập vào !!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
            try {
                ldto.setMaLuong(salaryIDField.getText());
                try {
                    ldto.setLuongCoBan(Double.parseDouble(salaryField.getText()));
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Lương bạn vừa nhập không phải là số, vui lòng nhập đúng !!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                lbus.addLuong(ldto);
                JOptionPane.showMessageDialog(this, "Thêm lương thành công !!!");
                defaultTableModel.setRowCount(0);
                setSalaryData(lbus.getLuong());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Mã lương bị trùng", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
    }//GEN-LAST:event_salaryAddBtnActionPerformed

    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        String input = inputField.getText().trim();
        if ("".equals(inputField.getText())) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mã KTKL cầm tìm !!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        dataTable.removeAll();
        defaultTableModel.setRowCount(0);
        setSalaryDataByID(lbus.getLuong(), input);
    }//GEN-LAST:event_searchBtnActionPerformed

    public void setSalaryDataByID(ArrayList<LuongDTO> Salarys, String MaLuong) {
        for (LuongDTO Salary : Salarys) {
            MaLuong = MaLuong.toLowerCase();
            if (Salary.getMaLuong().toLowerCase().equals(MaLuong)) {
                defaultTableModel.addRow(new Object[]{
                    Salary.getMaLuong(),
                    Salary.getLuongCoBan(),
                });
            }
        }
    }
    
