    private void salarySaveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salarySaveBtnActionPerformed
        // TODO add your handling code here:
        int row = dataTable.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn lương cần sửa !!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            try {
                ldto.setMaLuong(salaryIDField.getText());
                ldto.setLuongCoBan(Double.parseDouble(salaryField.getText()));
                lbus.updateLuong(ldto);
                JOptionPane.showMessageDialog(this, "Sửa lương thành công !!!");
                defaultTableModel.setRowCount(0);
                setSalaryData(lbus.getLuong());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Lương bạn vừa nhập không phải là số, vui lòng nhập đúng !!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    }//GEN-LAST:event_salarySaveBtnActionPerformed

