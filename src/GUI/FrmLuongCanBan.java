    public void showInfo(){
        int row = dataTable.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn lương cần sửa !!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } else {
            String salaryCode = String.valueOf(dataTable.getValueAt(row, 0));
            ldto = lbus.findSalaryByID(salaryCode);
            salaryIDField.setText(ldto.getMaLuong());
            salaryIDField.setEnabled(false);
            salaryField.setText(String.valueOf(ldto.getLuongCoBan()));
        }
    }
    
